/**
 * Resolve a single `assignedTo`-style reference (number id, numeric
 * string, employee name string, or "auto") to a concrete
 * employee_assignments.id scoped to the caller's company. Throws
 * ValidationError when the reference cannot be resolved — callers
 * should let it bubble.
 */
async function resolveAssigneeRef(
  ref: string | number,
  companyId: number,
  taskType: string,
): Promise<number> {
  if (ref === "auto") {
    const smartResult = await loadBalanceAssign(companyId, taskType);
    if (smartResult) return smartResult.assignmentId;
    throw new ValidationError(
      "تعذّر التعيين التلقائي — لا يوجد موظف متاح في هذه الشركة",
      { field: "assignedTo", fix: "اختر موظفاً يدوياً" },
    );
  }
  const trimmed = String(ref).trim();
  const asNumber = Number(trimmed);
  const isInteger = /^-?\d+$/.test(trimmed) && Number.isInteger(asNumber) && asNumber > 0;
  if (isInteger) {
    const [scopedAssignment] = await rawQuery<{ id: number }>(
      `SELECT id FROM employee_assignments
       WHERE id = $1 AND "companyId" = $2 AND status = 'active'
       LIMIT 1`,
      [asNumber, companyId],
    );
    if (!scopedAssignment) {
      throw new ValidationError(`المسؤول رقم ${asNumber} غير موجود في هذه الشركة`, {
        field: "assignedTo",
        fix: "اختر موظفاً من القائمة",
      });
    }
    return scopedAssignment.id;
  }
  const resolved = await rawQuery<{ id: number }>(
    `SELECT a.id FROM employee_assignments a
     JOIN employees e ON e.id = a."employeeId"
     WHERE a."companyId" = $1
       AND (e.name = $2 OR e.email = $2)
       AND a.status = 'active'
     ORDER BY a."isPrimary" DESC, a."createdAt" DESC LIMIT 1`,
    [companyId, trimmed],
  );
  if (resolved.length === 0) {
    throw new ValidationError(`الموظف "${trimmed}" غير موجود`, {
      field: "assignedTo",
      fix: "اختر موظفاً من القائمة",
    });
  }
  return resolved[0].id;
}

/**
 * Fetch the current team for a task (active assignees only) with
 * employee display names. Returns rows ordered with primary first.
 */
async function fetchTaskAssignees(
  taskId: number,
  companyId: number,
): Promise<TaskAssigneeRow[]> {
  return await rawQuery<TaskAssigneeRow>(
    `SELECT ta.id, ta."taskId", ta."assignmentId", ta.role,
            ta."assignedAt", ta."assignedBy", ta."removedAt",
            e.name AS "employeeName"
     FROM task_assignees ta
     LEFT JOIN employee_assignments ea ON ea.id = ta."assignmentId"
     LEFT JOIN employees e ON e.id = ea."employeeId" AND e."deletedAt" IS NULL
     WHERE ta."taskId" = $1 AND ta."companyId" = $2 AND ta."removedAt" IS NULL
     ORDER BY (ta.role = 'primary') DESC, ta."assignedAt" ASC`,
    [taskId, companyId],
  );
}

router.get("/", authorize({ feature: "tasks", action: "list" }), async (req, res) => {
  try {
    const scope = req.scope!;
    const { status = "", date = "", dateFrom = "", dateTo = "" } = req.query as Record<string, string | undefined>;

    const filters = parseScopeFilters(req);
    const { where: baseWhere, params, nextParamIndex } = buildScopedWhere(scope, filters, {
      companyColumn: 't."companyId"',
      branchColumn: 't."branchId"',
      enforceBranchScope: true,
    });
    let where = baseWhere + ` AND t."deletedAt" IS NULL`;
    let paramIdx = nextParamIndex;

    if (status) {
      where += ` AND t.status = $${paramIdx}`;
      params.push(status);
      paramIdx++;
    }

    if (date) {
      where += ` AND t."scheduledDate" = $${paramIdx}`;
      params.push(date);
      paramIdx++;
    }

    // #651 follow-up — normalize to createdAt per PR #653 template (was t."scheduledDate";
    // collided with the legacy `date` param which keeps scheduledDate semantics for the
    // calendar-day exact-match filter used by the today/tomorrow buttons).
    if (dateFrom) {
      where += ` AND t."createdAt" >= $${paramIdx}::timestamptz`;
      params.push(dateFrom);
      paramIdx++;
    }
    if (dateTo) {
      where += ` AND t."createdAt" <= ($${paramIdx}::date + INTERVAL '1 day')`;
      params.push(dateTo);
      paramIdx++;
    }

    if (scope.role === "employee" && !scope.isOwner && scope.activeAssignmentId) {
      // Multi-assignee scope: see tasks where I'm the primary OR a team
      // member. The OR-with-EXISTS pattern stays index-friendly thanks
      // to idx_task_assignees_assignment (companyId, assignmentId).
      where += ` AND (
        t."assignedTo" = $${paramIdx}
        OR EXISTS (
          SELECT 1 FROM task_assignees ta
          WHERE ta."taskId" = t.id AND ta."assignmentId" = $${paramIdx} AND ta."removedAt" IS NULL
        )
      )`;
      params.push(scope.activeAssignmentId);
      paramIdx++;
    }

    const { linkedEntityType, linkedEntityId, includeRelatedMaintenance, relatedUnitId } = req.query as Record<string, string | undefined>;
    if (linkedEntityType && linkedEntityId && includeRelatedMaintenance === "true" && linkedEntityType === "property_unit") {
      where += ` AND ((t."linkedEntityType" = $${paramIdx} AND t."linkedEntityId" = $${paramIdx + 1}) OR (t."linkedEntityType" = 'maintenance_request' AND t."linkedEntityId" IN (SELECT id FROM maintenance_requests WHERE "unitId" = $${paramIdx + 1} AND "companyId" = $${paramIdx + 2})))`;
      params.push(linkedEntityType, Number(linkedEntityId), scope.companyId);
      paramIdx += 3;
    } else {
      if (linkedEntityType) {
        where += ` AND t."linkedEntityType" = $${paramIdx}`;
        params.push(linkedEntityType);
        paramIdx++;
      }
      if (linkedEntityId) {
        where += ` AND t."linkedEntityId" = $${paramIdx}`;
        params.push(Number(linkedEntityId));
        paramIdx++;
      }
    }
    if (relatedUnitId && linkedEntityType === "maintenance_request") {
      where += ` AND t."linkedEntityId" IN (SELECT mr2.id FROM maintenance_requests mr2 WHERE mr2."unitId" = $${paramIdx} AND mr2."deletedAt" IS NULL)`;
      params.push(Number(relatedUnitId));
      paramIdx++;
    }

    // Pre-aggregate task_assignees counts once via CTE — the original
    // correlated subquery (SELECT COUNT(*) ... WHERE taskId = t.id)
    // was N+1: postgres planned one execution per returned task, so
    // 500 tasks == 501 lookups through task_assignees. This CTE scans
    // the junction table once and joins per-task counts back.
    const tasks = await rawQuery<TaskListRow & { creatorName?: string | null; assigneeCount?: number | null; createdBy?: number | null }>(
      `WITH assignee_counts AS (
         SELECT "taskId", COUNT(*) AS "assigneeCount"
         FROM task_assignees
         WHERE "removedAt" IS NULL
         GROUP BY "taskId"
       )
       SELECT t.id, t.title, t.description, t.status, t.priority, t.type,
              t."scheduledDate", t."scheduledStart", t."scheduledEnd",
              t."completedAt", t."createdAt", t.notes,
              t."clientId", t."createdBy",
              t."linkedEntityType", t."linkedEntityId", t."autoGenerated",
              e.name AS "assigneeName",
              creator.name AS "creatorName",
              COALESCE(ac."assigneeCount", 0)::int AS "assigneeCount",
              c.name AS "clientName", c.phone AS "clientPhone"
       FROM tasks t
       LEFT JOIN employee_assignments ea ON ea.id = t."assignedTo"
       LEFT JOIN employees e ON e.id = ea."employeeId" AND e."companyId" = ea."companyId" AND e."deletedAt" IS NULL
       LEFT JOIN employee_assignments cea ON cea.id = t."createdBy"
       LEFT JOIN employees creator ON creator.id = cea."employeeId" AND creator."companyId" = cea."companyId" AND creator."deletedAt" IS NULL
       LEFT JOIN clients c ON c.id = t."clientId" AND c."companyId" = t."companyId" AND c."deletedAt" IS NULL
       LEFT JOIN assignee_counts ac ON ac."taskId" = t.id
       WHERE ${where}
       ORDER BY t.priority DESC, t."scheduledDate" ASC NULLS LAST
       LIMIT 500`,
      params
    );

    // Resolve linkedEntityName in bulk: group ids by type, then issue
    // one tenant-scoped query per distinct type (small constant set).
    const idsByType = new Map<string, Set<number>>();
    for (const task of tasks) {
      if (task.linkedEntityType && task.linkedEntityId) {
        if (!idsByType.has(task.linkedEntityType)) idsByType.set(task.linkedEntityType, new Set());
        idsByType.get(task.linkedEntityType)!.add(Number(task.linkedEntityId));
      }
    }
    const bulkQueryMap: Record<string, string> = {
      property_unit: `SELECT id, "unitNumber" AS name FROM property_units WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
      vehicle: `SELECT id, COALESCE("plateNumber", make || ' ' || model) AS name FROM fleet_vehicles WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
      client: `SELECT id, name FROM clients WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
      project: `SELECT id, name FROM projects WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
      contract: `SELECT id, COALESCE("contractNumber", 'عقد #' || id) AS name FROM rental_contracts WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
      legal_case: `SELECT id, COALESCE(title, "caseNumber", 'قضية #' || id) AS name FROM legal_cases WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
      maintenance_request: `SELECT id, COALESCE(description, category, 'طلب #' || id) AS name FROM maintenance_requests WHERE id = ANY($1) AND "companyId"=$2 AND "deletedAt" IS NULL`,
    };
    const namesByType = new Map<string, Map<number, string>>();
    await Promise.all(
      Array.from(idsByType.entries()).map(async ([type, idSet]) => {
        const sql = bulkQueryMap[type];
        if (!sql) return;
        try {
          const rows = await rawQuery<{ id: number; name: string }>(sql, [Array.from(idSet), scope.companyId]);
          const map = new Map<number, string>();
          for (const r of rows) map.set(Number(r.id), r.name);
          namesByType.set(type, map);
        } catch (e) { logger.warn(e, "tasks: linked entity table may not exist"); }
      })
    );
    for (const task of tasks) {
      if (task.linkedEntityType && task.linkedEntityId) {
        task.linkedEntityName = namesByType.get(task.linkedEntityType)?.get(Number(task.linkedEntityId)) ?? null;
      }
    }

    res.json(maskFields(req, tasks));
  } catch (err) {
    handleRouteError(err, res, "List tasks error:");
  }
});

