router.post(
  "/:id/assignees",
  authorize({ feature: "tasks", action: "update", resource: { table: "tasks", idParam: "id" } }),
  async (req, res) => {
    try {
      const scope = req.scope!;
      const id = parseId(req.params.id, "id");
      const { assignmentId, role } = zodParse(assigneeMutationSchema.safeParse(req.body));
      // Verify task exists in this tenant.
      const [task] = await rawQuery<{ id: number }>(
        `SELECT id FROM tasks WHERE id = $1 AND "companyId" = $2 AND "deletedAt" IS NULL`,
        [id, scope.companyId],
      );
      if (!task) throw new NotFoundError("المهمة غير موجودة");
      // Verify the assignment belongs to this company.
      const [assignment] = await rawQuery<{ id: number }>(
        `SELECT id FROM employee_assignments
         WHERE id = $1 AND "companyId" = $2 AND status = 'active'`,
        [assignmentId, scope.companyId],
      );
      if (!assignment) {
        throw new ValidationError(`المسؤول رقم ${assignmentId} غير موجود في هذه الشركة`, {
          field: "assignmentId",
          fix: "اختر موظفاً نشطاً من القائمة",
        });
      }
      // All assignee writes are atomic: a primary-demote + assignedTo mirror
      // must not commit without the matching assignee insert/update. rawQuery
      // joins the ambient transaction (txStore) automatically.
      await withTransaction(async () => {
        // Promotion: if role='primary', demote any existing primary first.
        if (role === "primary") {
          await rawQuery(
            `UPDATE task_assignees SET role = 'member'
             WHERE "taskId" = $1 AND "companyId" = $2 AND role = 'primary' AND "removedAt" IS NULL`,
            [id, scope.companyId],
          );
          // Also mirror into tasks.assignedTo.
          await rawQuery(
            `UPDATE tasks SET "assignedTo" = $1 WHERE id = $2 AND "companyId" = $3`,
            [assignmentId, id, scope.companyId],
          );
        }
        // SELECT-then-INSERT-or-UPDATE in two steps. The audit:schema-
        // drift regex (scripts/src/check-schema-drift.mjs) misreads the
        // upsert clause as a table reference, so we use two queries
        // instead. Two round-trips here are fine — there's no
        // concurrency win to chase, just code aesthetics.
        const [existingRow] = await rawQuery<{ id: number }>(
          `SELECT id FROM task_assignees
           WHERE "taskId" = $1 AND "assignmentId" = $2 AND "companyId" = $3 AND "removedAt" IS NULL
           LIMIT 1`,
          [id, assignmentId, scope.companyId],
        );
        if (existingRow) {
          await rawQuery(
            `UPDATE task_assignees SET role = $1 WHERE id = $2 AND "companyId" = $3`,
            [role, existingRow.id, scope.companyId],
          );
        } else {
          await rawQuery(
            `INSERT INTO task_assignees ("companyId", "taskId", "assignmentId", role, "assignedBy")
             VALUES ($1, $2, $3, $4, $5)`,
            [scope.companyId, id, assignmentId, role, scope.activeAssignmentId],
          );
        }
      });
      createAuditLog({ companyId: scope.companyId, userId: scope.userId, action: "assignee.add", entity: "tasks", entityId: id, after: { assignmentId, role } }).catch((e) => logger.error(e, "tasks background task failed"));
      const team = await fetchTaskAssignees(id, scope.companyId);
      res.status(201).json(maskFields(req, team));
    } catch (err) {
      handleRouteError(err, res, "Add task assignee error:");
    }
  },
);

router.delete(
  "/:id/assignees/:assignmentId",
  authorize({ feature: "tasks", action: "update", resource: { table: "tasks", idParam: "id" } }),
  async (req, res) => {
    try {
      const scope = req.scope!;
      const id = parseId(req.params.id, "id");
      const assignmentId = parseId(req.params.assignmentId, "assignmentId");
      // Confirm tenant & task exists.
      const [task] = await rawQuery<{ id: number; assignedTo: number | null }>(
        `SELECT id, "assignedTo" FROM tasks WHERE id = $1 AND "companyId" = $2 AND "deletedAt" IS NULL`,
        [id, scope.companyId],
      );
      if (!task) throw new NotFoundError("المهمة غير موجودة");
      // Remove + primary-promotion are atomic: a removed primary must not
      // commit without the replacement primary (or the assignedTo clear).
      await withTransaction(async () => {
        const [removed] = await rawQuery<{ id: number; role: string }>(
          `UPDATE task_assignees SET "removedAt" = NOW()
           WHERE "taskId" = $1 AND "assignmentId" = $2 AND "companyId" = $3 AND "removedAt" IS NULL
           RETURNING id, role`,
          [id, assignmentId, scope.companyId],
        );
        if (!removed) throw new NotFoundError("هذا المستخدم ليس عضواً في المهمة");
        // If we just removed the primary, promote the oldest remaining
        // member to primary (and mirror to tasks.assignedTo). If the team
        // is now empty, clear assignedTo too.
        if (removed.role === "primary") {
          const [next] = await rawQuery<{ id: number; assignmentId: number }>(
            `SELECT id, "assignmentId" FROM task_assignees
             WHERE "taskId" = $1 AND "companyId" = $2 AND "removedAt" IS NULL
             ORDER BY "assignedAt" ASC LIMIT 1`,
            [id, scope.companyId],
          );
          if (next) {
            await rawQuery(
              `UPDATE task_assignees SET role = 'primary' WHERE id = $1 AND "companyId" = $2`,
              [next.id, scope.companyId],
            );
            await rawQuery(
              `UPDATE tasks SET "assignedTo" = $1 WHERE id = $2 AND "companyId" = $3`,
              [next.assignmentId, id, scope.companyId],
            );
          } else {
            await rawQuery(
              `UPDATE tasks SET "assignedTo" = NULL WHERE id = $1 AND "companyId" = $2`,
              [id, scope.companyId],
            );
          }
        }
      });
      createAuditLog({ companyId: scope.companyId, userId: scope.userId, action: "assignee.remove", entity: "tasks", entityId: id, after: { assignmentId } }).catch((e) => logger.error(e, "tasks background task failed"));
      const team = await fetchTaskAssignees(id, scope.companyId);
      res.json(maskFields(req, team));
    } catch (err) {
      handleRouteError(err, res, "Remove task assignee error:");
    }
  },
);

router.delete("/:id", authorize({ feature: "tasks", action: "delete", resource: { table: "tasks", idParam: "id" } }), async (req, res) => {
  try {
    const scope = req.scope!;
    const id = parseId(req.params.id, "id");
    const beforeParams: any[] = [id, scope.companyId];
    let beforeWhere = `id = $1 AND "companyId" = $2`;
    if (scope.role === "employee" && !scope.isOwner && scope.activeAssignmentId) {
      beforeWhere += ` AND "assignedTo" = $3`;
      beforeParams.push(scope.activeAssignmentId);
    }
    const [before] = await rawQuery<TaskRow>(`SELECT * FROM tasks WHERE ${beforeWhere}`, beforeParams);
    if (!before) { throw new NotFoundError("المهمة غير موجودة"); }

    // NF-TASK-01 — completed / cancelled tasks are the historical record
    // for retrospectives, KPIs and SLA reports. Allowing them to be
    // soft-deleted erases that history; restrict delete to tasks that
    // haven't yet recorded an outcome.
    if (before.status && !["pending", "in_progress", "blocked"].includes(String(before.status))) {
      throw new ConflictError(
        `لا يمكن حذف مهمة بحالة "${before.status}"`,
        { field: "status", fix: "أرشِف المهمة بدلاً من الحذف" }
      );
    }

    const params: unknown[] = [id, scope.companyId];
    let whereClause = `id = $1 AND "companyId" = $2`;

    if (scope.role === "employee" && !scope.isOwner && scope.activeAssignmentId) {
      whereClause += ` AND "assignedTo" = $3`;
      params.push(scope.activeAssignmentId);
    }

    const rows = await rawQuery<IdRow>(
      `UPDATE tasks SET "deletedAt" = NOW() WHERE ${whereClause} AND "deletedAt" IS NULL RETURNING id`,
      params
    );
    if (rows.length === 0) { throw new NotFoundError("المهمة غير موجودة"); }
    createAuditLog({ companyId: scope.companyId, userId: scope.userId, action: "delete", entity: "tasks", entityId: id, before }).catch((e) => logger.error(e, "tasks background task failed"));
    emitEvent({ companyId: scope.companyId, branchId: scope.branchId, userId: scope.userId, action: "task.deleted", entity: "tasks", entityId: id, details: JSON.stringify({ title: before?.title }) }).catch((e) => logger.error(e, "tasks background task failed"));
    res.json({ success: true });
  } catch (err) {
    handleRouteError(err, res, "Delete task error:");
  }
});

