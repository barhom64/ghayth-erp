router.patch("/:id", authorize({ feature: "tasks", action: "update", resource: { table: "tasks", idParam: "id" } }), async (req, res) => {
  try {
    const scope = req.scope!;
    const id = parseId(req.params.id, "id");
    const { title, description, type, priority, status, scheduledStart, scheduledEnd, scheduledDate, notes, assignedTo: bodyAssignedTo, assignees: bodyAssignees } = zodParse(updateTaskSchema.safeParse(req.body));
    // كمرآة لتحقّق POST: لا يجوز أن تصبح نهاية الجدولة قبل بدايتها بعد التعديل الجزئي.
    if (scheduledStart !== undefined || scheduledEnd !== undefined) {
      const [cur] = await rawQuery<{ scheduledStart: string | null; scheduledEnd: string | null }>(
        `SELECT "scheduledStart", "scheduledEnd" FROM tasks WHERE id = $1 AND "companyId" = $2 AND "deletedAt" IS NULL`,
        [id, scope.companyId],
      );
      const ss = scheduledStart ?? cur?.scheduledStart;
      const se = scheduledEnd ?? cur?.scheduledEnd;
      if (ss && se && new Date(se) < new Date(ss)) {
        throw new ValidationError("تاريخ النهاية قبل تاريخ البداية", { field: "scheduledEnd", fix: "اختر موعد انتهاء بعد موعد البداية" });
      }
    }
    const sets: string[] = [];
    const params: unknown[] = [];
    let idx = 1;

    const addField = (col: string, val: unknown) => {
      if (val !== undefined) { sets.push(`"${col}" = $${idx}`); params.push(val); idx++; }
    };

    addField("title", title);
    addField("description", description);
    addField("type", type);
    addField("priority", priority);
    addField("status", status);
    addField("scheduledStart", scheduledStart);
    addField("scheduledEnd", scheduledEnd);
    addField("scheduledDate", scheduledDate);
    addField("notes", notes);

    // Reassign primary owner if the caller provided assignedTo OR the
    // first element of assignees. The full-team replacement happens
    // after the UPDATE; here we only mirror the primary into the legacy
    // tasks.assignedTo column.
    let primaryReassignTo: number | null | undefined;
    if (bodyAssignees !== undefined) {
      // Resolve team — first element becomes primary. Empty array
      // clears the primary mirror.
      if (bodyAssignees.length === 0) {
        primaryReassignTo = null;
      } else {
        primaryReassignTo = await resolveAssigneeRef(
          bodyAssignees[0],
          scope.companyId,
          type || "task",
        );
      }
    } else if (bodyAssignedTo !== undefined && bodyAssignedTo !== null && String(bodyAssignedTo).trim() !== "") {
      primaryReassignTo = await resolveAssigneeRef(
        bodyAssignedTo,
        scope.companyId,
        type || "task",
      );
    } else if (bodyAssignedTo === null) {
      primaryReassignTo = null;
    }
    if (primaryReassignTo !== undefined) {
      addField("assignedTo", primaryReassignTo);
    }

    if (status === "completed") {
      sets.push(`"completedAt" = NOW()`);
    }

    if (sets.length === 0 && bodyAssignees === undefined) {
      throw new ValidationError("لا توجد بيانات للتحديث");
    }

    let currentStatus: string | undefined;
    if (status) {
      const [current] = await rawQuery<{ status: string }>(
        `SELECT status FROM tasks WHERE id = $1 AND "companyId" = $2 AND "deletedAt" IS NULL`,
        [id, scope.companyId]
      );
      if (!current) throw new NotFoundError("المهمة غير موجودة");
      currentStatus = current.status;
      const allowed = VALID_TASK_TRANSITIONS[currentStatus];
      if (allowed && !allowed.includes(status)) {
        throw new ConflictError(`لا يمكن نقل المهمة من "${currentStatus}" إلى "${status}"`);
      }
    }

    params.push(id);
    let whereClause = `id = $${idx}`;
    idx++;

    params.push(scope.companyId);
    whereClause += ` AND "companyId" = $${idx}`;
    idx++;

    if (currentStatus) {
      params.push(currentStatus);
      whereClause += ` AND status = $${idx}`;
      idx++;
    }

    if (scope.role === "employee" && !scope.isOwner && scope.activeAssignmentId) {
      // Multi-assignee: allow update if user is primary OR a team
      // member. EXISTS subquery is anchored on the task id parameter
      // (already bound at $1 from `params.push(id)` above) so we don't
      // collide with the unqualified UPDATE column references.
      whereClause += ` AND (
        tasks."assignedTo" = $${idx}
        OR EXISTS (
          SELECT 1 FROM task_assignees ta
          WHERE ta."taskId" = tasks.id
            AND ta."companyId" = tasks."companyId"
            AND ta."assignmentId" = $${idx}
            AND ta."removedAt" IS NULL
        )
      )`;
      params.push(scope.activeAssignmentId);
      idx++;
    }

    // If only the team is being modified (no scalar field updates),
    // touch updatedAt so the ownership check still runs through UPDATE.
    if (sets.length === 0) {
      sets.push(`"updatedAt" = NOW()`);
    }
    // Scalar update + team replacement are atomic: a soft-remove of old
    // assignees must not commit without the matching re-role/insert of the
    // new team (or the tasks row update). rawQuery joins the ambient
    // transaction (txStore) automatically.
    const rows = await withTransaction(async () => {
      const updated = await rawQuery<TaskRow>(
        `UPDATE tasks SET ${sets.join(", ")} WHERE ${whereClause} RETURNING *`,
        params,
      );
      if (updated.length === 0) { throw new NotFoundError("المهمة غير موجودة"); }

      // Replace the assignee team if `assignees` was sent. We don't
      // truncate-and-rewrite (audit log integrity); we soft-remove rows
      // no longer in the new list and insert any new ones.
      if (bodyAssignees !== undefined) {
        const resolved: number[] = [];
        const seen = new Set<number>();
        for (const ref of bodyAssignees) {
          const aid = await resolveAssigneeRef(ref, scope.companyId, type || "task");
          if (!seen.has(aid)) {
            seen.add(aid);
            resolved.push(aid);
          }
        }
        const existing = await rawQuery<{ assignmentId: number; role: string }>(
          `SELECT "assignmentId", role FROM task_assignees
           WHERE "taskId" = $1 AND "companyId" = $2 AND "removedAt" IS NULL`,
          [id, scope.companyId],
        );
        const existingSet = new Set(existing.map((r) => r.assignmentId));
        // Remove rows that are no longer in the new team.
        for (const e of existing) {
          if (!resolved.includes(e.assignmentId)) {
            await rawQuery(
              `UPDATE task_assignees SET "removedAt" = NOW()
               WHERE "taskId" = $1 AND "assignmentId" = $2 AND "companyId" = $3 AND "removedAt" IS NULL`,
              [id, e.assignmentId, scope.companyId],
            );
          }
        }
        // Insert / re-role.
        for (let i = 0; i < resolved.length; i++) {
          const role = i === 0 ? "primary" : "member";
          if (existingSet.has(resolved[i])) {
            // Update role on the existing active row.
            await rawQuery(
              `UPDATE task_assignees SET role = $1
               WHERE "taskId" = $2 AND "assignmentId" = $3 AND "companyId" = $4 AND "removedAt" IS NULL`,
              [role, id, resolved[i], scope.companyId],
            );
          } else {
            await rawQuery(
              `INSERT INTO task_assignees ("companyId", "taskId", "assignmentId", role, "assignedBy")
               VALUES ($1, $2, $3, $4, $5) ON CONFLICT DO NOTHING`,
              [scope.companyId, id, resolved[i], role, scope.activeAssignmentId],
            );
          }
        }
      }
      return updated;
    });

    createAuditLog({ companyId: scope.companyId, userId: scope.userId, action: "update", entity: "tasks", entityId: id, after: { title, description, type, priority, status, scheduledStart, scheduledEnd, scheduledDate, notes, assignedTo: primaryReassignTo, assignees: bodyAssignees } }).catch((e) => logger.error(e, "tasks background task failed"));

    if (status) {
      emitEvent({
        companyId: scope.companyId,
        branchId: scope.branchId,
        userId: scope.userId,
        action: `task.${status}`,
        entity: "tasks",
        entityId: id,
        after: {
          status,
          ...(status === "completed" ? { completedAt: rows[0]?.completedAt ?? new Date().toISOString() } : {}),
          assignedTo: rows[0]?.assignedTo ?? null,
          title: rows[0]?.title ?? null,
        },
      }).catch((e) => logger.error(e, "tasks background task failed"));
    } else if (sets.length > 0) {
      emitEvent({
        companyId: scope.companyId,
        branchId: scope.branchId,
        userId: scope.userId,
        action: "task.updated",
        entity: "tasks",
        entityId: id,
        after: { title: rows[0]?.title ?? null },
      }).catch((e) => logger.error(e, "tasks background task failed"));
    }

    const team = await fetchTaskAssignees(id, scope.companyId);
    res.json({ ...rows[0], assignees: team });
  } catch (err) {
    handleRouteError(err, res, "Update task error:");
  }
});

