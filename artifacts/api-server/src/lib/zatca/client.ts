/**
 * Onboarding: promote a compliance CSID to production after the test
 * pack passes. ZATCA returns a fresh binarySecurityToken + secret
 * scoped to production.
 */
export async function requestProductionCsid(opts: {
  env: ZatcaEnvironment;
  /** The compliance CSID's credentials — used to authenticate THIS call. */
  complianceCreds: ZatcaCredentials;
  /** ZATCA's request ID from the original compliance issuance. */
  complianceRequestId: string;
  signal?: AbortSignal;
}): Promise<{ binarySecurityToken: string; secret: string }> {
  const url = buildFatoraUrl(opts.env, PRODUCTION_CSID_PATH);
  const body = JSON.stringify({ compliance_request_id: opts.complianceRequestId });
  const headers = {
    ...commonFatoraHeaders(),
    Authorization: basicAuthHeader(opts.complianceCreds),
  };
  const json = await postJson(url, body, headers, opts.signal);
  return {
    binarySecurityToken: String(json?.binarySecurityToken ?? ""),
    secret: String(json?.secret ?? ""),
  };
}

// ─────────────────────────────────────────────────────────────────────
// Internal: POST helper with timeout + structured transport errors
// ─────────────────────────────────────────────────────────────────────

async function postJson(
  url: string,
  body: string,
  headers: Record<string, string>,
  externalSignal?: AbortSignal,
): Promise<Record<string, unknown>> {
  const controller = new AbortController();
  const timeoutId = setTimeout(
    () => controller.abort(new Error(`ZATCA request timed out after ${DEFAULT_TIMEOUT_MS}ms`)),
    DEFAULT_TIMEOUT_MS,
  );
  // Plumb the external signal through too so callers can cancel.
  externalSignal?.addEventListener("abort", () => controller.abort(externalSignal.reason));

  try {
    const response = await fetch(url, {
      method: "POST",
      body,
      headers,
      signal: controller.signal,
    });

    const text = await response.text();
    let parsed: unknown;
    try {
      parsed = text ? JSON.parse(text) : {};
    } catch {
      throw new ZatcaTransportError(
        `ZATCA returned non-JSON response (${response.status})`,
        response.status,
        text.slice(0, 2000),
      );
    }

    if (!response.ok) {
      // Surface the body for audit / debugging. ZATCA usually returns
      // structured validationResults even on 4xx, so we let the parser
      // see them too via the throw payload.
      throw new ZatcaTransportError(
        `ZATCA returned HTTP ${response.status}`,
        response.status,
        parsed,
      );
    }

    return parsed as Record<string, unknown>;
  } finally {
    clearTimeout(timeoutId);
  }
}

/**
 * Transport-level error from a ZATCA call. Caller decides whether to
 * retry (5xx) or surface to the user (4xx). The `body` field carries
 * the parsed (or truncated) response so the audit log captures what
 * ZATCA actually said.
 */
export class ZatcaTransportError extends Error {
  constructor(
    message: string,
    public readonly httpStatus: number,
    public readonly body: unknown,
  ) {
    super(message);
    this.name = "ZatcaTransportError";
  }
}
