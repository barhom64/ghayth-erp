/**
 * Onboarding: request a compliance CSID using a CSR + the OTP the
 * seller generated from the ZATCA portal. Anonymous endpoint — no
 * Basic auth header.
 */
export async function requestComplianceCsid(opts: {
  env: ZatcaEnvironment;
  csrBase64: string;
  otp: string;
  signal?: AbortSignal;
}): Promise<{ binarySecurityToken: string; secret: string; requestId: string }> {
  const url = buildFatoraUrl(opts.env, COMPLIANCE_CSID_PATH);
  const headers = {
    ...commonFatoraHeaders(),
    OTP: opts.otp,
  };
  const body = JSON.stringify({ csr: opts.csrBase64 });
  const json = await postJson(url, body, headers, opts.signal);
  return {
    binarySecurityToken: String(json?.binarySecurityToken ?? ""),
    secret: String(json?.secret ?? ""),
    requestId: String(json?.requestID ?? json?.requestId ?? ""),
  };
}

