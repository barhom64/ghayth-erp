import { describe, it, expect } from "vitest";
import { readFileSync } from "node:fs";
import { join } from "node:path";

const REPO_ROOT = join(import.meta.dirname!, "../../../..");
const read = (p: string) => readFileSync(join(REPO_ROOT, p), "utf8");

// ─── Renewal flow contract — claude/hr-driver-integrated-view branch ─────
// Locks the cross-module contract introduced over 22 commits on this
// branch: every duration-tracked artefact (company documents, fleet
// insurance, employee documents, bank guarantees, legal contracts,
// rental contracts, smart expenses) must call registerObligationWithTask
// (NOT just registerObligation) so the calendar entry comes with a
// matching task on the responsible department's queue. The bridge
// helper itself must seed an rbac_user_roles row when none exists,
// and the close-the-loop renew endpoint on company documents must
// post an expense JE + register the next-cycle obligation atomically.

const BRIDGE = read("artifacts/api-server/src/lib/obligationTaskBridge.ts");
const HR = read("artifacts/api-server/src/routes/hr.ts");
const FLEET = read("artifacts/api-server/src/routes/fleet.ts");
const FINANCE_JOURNAL = read("artifacts/api-server/src/routes/finance-journal.ts");
const FINANCE_HARDENING = read("artifacts/api-server/src/routes/finance-hardening.ts");
const LEGAL = read("artifacts/api-server/src/routes/legal.ts");
const PROPERTIES = read("artifacts/api-server/src/routes/properties.ts");
const ENSURE_ROLE = read("artifacts/api-server/src/lib/rbac/ensureRole.ts");

describe("obligationTaskBridge — helper contract", () => {
  it("exports registerObligationWithTask", () => {
    expect(BRIDGE).toMatch(/export\s+async\s+function\s+registerObligationWithTask/);
  });

  it("registers the obligation by delegating to obligationsEngine", () => {
    expect(BRIDGE).toContain('from "./obligationsEngine.js"');
    expect(BRIDGE).toMatch(/await\s+registerObligation\s*\(\s*input\s*\)/);
  });

  it("opens a tasks row with type='renewal' + autoGenerated=true", () => {
    expect(BRIDGE).toContain('INSERT INTO tasks');
    expect(BRIDGE).toContain("'renewal'");
    expect(BRIDGE).toContain("true"); // autoGenerated flag
    expect(BRIDGE).toContain('"linkedEntityType"');
    expect(BRIDGE).toContain('"linkedEntityId"');
  });

  it("is idempotent — skips if a pending/in_progress renewal task already exists", () => {
    expect(BRIDGE).toMatch(/status IN \('pending', 'in_progress'\)/);
  });

  it("resolves assignee from entityType → role fallback map", () => {
    // The map covers the 7 entity types we wired to the bridge.
    expect(BRIDGE).toContain("company_document");
    expect(BRIDGE).toContain("hr_manager");
    expect(BRIDGE).toContain("fleet_vehicle");
    expect(BRIDGE).toContain("fleet_manager");
    expect(BRIDGE).toContain("bank_guarantee");
    expect(BRIDGE).toContain("finance_manager");
    expect(BRIDGE).toContain("legal_contract");
    expect(BRIDGE).toContain("legal_manager");
    expect(BRIDGE).toContain("rental_contract");
    expect(BRIDGE).toContain("property_manager");
    expect(BRIDGE).toContain("employee_document");
    expect(BRIDGE).toContain("expense");
  });

  it("falls back to department manager when responsibleDepartmentId is supplied", () => {
    expect(BRIDGE).toContain("responsibleDepartmentId");
    expect(BRIDGE).toContain("departments d");
    expect(BRIDGE).toContain('d."managerId"');
  });
});

