describe("employee POST binds new users to rbac_user_roles via ensureRbacRoleByKey", () => {
  it("employees.ts imports ensureRbacRoleByKey", () => {
    const employees = read("artifacts/api-server/src/routes/employees.ts");
    expect(employees).toContain('from "../lib/rbac/ensureRole.js"');
  });

  it("employees POST resolves an effectiveRole then uses it for users.role + rbac_user_roles", () => {
    const employees = read("artifacts/api-server/src/routes/employees.ts");
    // The effectiveRole variable replaces the body.role on both the
    // assignment INSERT, the users INSERT, and the rbac binding.
    expect(employees).toContain("let effectiveRole = role;");
    // assignment INSERT uses effectiveRole
    expect(employees).toMatch(/INSERT INTO employee_assignments[\s\S]{1,400}effectiveRole/);
    // users INSERT uses effectiveRole
    expect(employees).toMatch(/INSERT INTO users[\s\S]{1,400}effectiveRole/);
    // rbac binding uses effectiveRole and the ensure helper
    expect(employees).toContain("ensureRbacRoleByKey");
    expect(employees).toContain("INSERT INTO rbac_user_roles");
  });
});

