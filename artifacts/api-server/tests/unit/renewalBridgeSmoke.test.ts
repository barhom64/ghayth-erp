describe("close-the-loop renew endpoint — company documents", () => {
  it("hr.ts exposes POST /company-documents/:id/renew", () => {
    expect(HR).toContain('post("/company-documents/:id/renew"');
  });

  it("renew handler shifts expiryDate forward", () => {
    const idx = HR.indexOf('post("/company-documents/:id/renew"');
    expect(idx).toBeGreaterThan(-1);
    const block = HR.slice(idx, idx + 6000);
    expect(block).toContain('UPDATE company_documents');
    expect(block).toContain('"expiryDate" = $1');
  });

  it("renew handler closes the old obligation via markObligationMet", () => {
    const idx = HR.indexOf('post("/company-documents/:id/renew"');
    const block = HR.slice(idx, idx + 6000);
    expect(block).toContain("markObligationMet");
    expect(block).toContain('"company_document"');
    expect(block).toContain('"renewal"');
  });

  it("renew handler posts the optional renewal-fee expense JE", () => {
    const idx = HR.indexOf('post("/company-documents/:id/renew"');
    const block = HR.slice(idx, idx + 6000);
    expect(block).toContain("postJournalEntry");
    expect(block).toContain("postExpense");
    // sourceKey includes the new expiry so the same JE can't post twice.
    expect(block).toContain("company_document_renewal_paid:");
  });

  it("renew handler registers the next-cycle obligation+task", () => {
    const idx = HR.indexOf('post("/company-documents/:id/renew"');
    const block = HR.slice(idx, idx + 6000);
    expect(block).toContain("registerObligationWithTask");
    // The dedupeKey for the new cycle includes the new expiry so the
    // engine creates a fresh row (template literal, not a plain string).
    expect(block).toContain("`company_document:${id}:${b.newExpiryDate}`");
  });
});

