// Applies a validated proposal: dry-run = save to .agent-state/proposed/.
// live = write files locally, run typecheck, then hand to _pr_push.

import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { writeProposed, appendFixLog } from "./lib/state.mjs";

const REPO_ROOT = new URL("../../", import.meta.url).pathname;

export function dryRunApply({ finding, context, proposal }) {
  const slug = `${finding.collector}_${finding.key}`;
  const file = writeProposed(slug, { finding, context: stripContext(context), proposal });
  appendFixLog({ action: "dry-run-proposed", errorKey: finding.key, file, summary: proposal.summary });
  return { mode: "dry-run", proposedFile: file };
}

function stripContext(ctx) {
  // Keep proposal context small in the saved file
  return {
    finding: ctx.finding,
    files: Object.fromEntries(Object.entries(ctx.files || {}).map(([k, v]) => [k, v ? { path: v.path, length: v.content?.length || 0 } : null])),
    liveDb: ctx.liveDb,
    recentMigrations: ctx.recentMigrations,
  };
}

export function liveApply({ finding, proposal, branchPrefix }) {
  // Write files to working tree
  for (const f of proposal.files) {
    const abs = path.join(REPO_ROOT, f.path);
    fs.mkdirSync(path.dirname(abs), { recursive: true });
    fs.writeFileSync(abs, f.content);
  }

  // Local sanity gate before pushing — typecheck
  const tc = spawnSync("pnpm", ["run", "typecheck:libs"], {
    cwd: REPO_ROOT, encoding: "utf8", timeout: 240_000,
  });
  if (tc.status !== 0) {
    appendFixLog({ action: "typecheck-failed", errorKey: finding.key, stderr: (tc.stderr || tc.stdout || "").slice(-800) });
    // Revert by re-checking out via git is blocked here; rely on guard CI as final gate.
    return { mode: "live", success: false, reason: "typecheck-failed" };
  }

  // Hand off to _pr_push by writing the canonical state file AND running the
  // pusher synchronously, so each finding gets its own end-to-end PR attempt
  // with no race against parallel passes / the standalone PR Push workflow.
  // (Lockfile prevents two self-heal runs from clobbering one state file.)
  const branch = `${branchPrefix}${finding.collector}-${finding.key}-${Date.now().toString(36)}`;
  const pushState = {
    branch,
    title: proposal.prTitle || `fix(self-heal): ${proposal.summary || finding.id}`,
    body: buildPrBody({ finding, proposal }),
    files: proposal.files.map((f) => f.path),
  };
  const stateFile = process.env.PR_PUSH_STATE || "/tmp/_pr_push_state.json";
  const lockFile = `${stateFile}.lock`;

  // Cheap mutex — fail closed if another writer is mid-push.
  try {
    fs.writeFileSync(lockFile, String(process.pid), { flag: "wx" });
  } catch (e) {
    appendFixLog({ action: "live-failed-locked", errorKey: finding.key, lockFile });
    return { mode: "live", success: false, reason: "pr-push-locked" };
  }

  try {
    fs.writeFileSync(stateFile, JSON.stringify(pushState, null, 2));
    appendFixLog({ action: "pr-push-state-written", errorKey: finding.key, branch, stateFile });
    // Synchronously invoke the pusher so we get a definitive result before the
    // next finding overwrites the state file.
    const r = spawnSync("node", ["scripts/_pr_push.mjs"], {
      cwd: REPO_ROOT, encoding: "utf8", timeout: 30 * 60_000, stdio: "inherit",
    });
    if (r.status !== 0) {
      appendFixLog({ action: "live-failed-pr-push-failed", errorKey: finding.key, branch, exit: r.status });
      return { mode: "live", success: false, reason: "pr-push-failed" };
    }
    return { mode: "live", success: true, branch, stateFile };
  } finally {
    try { fs.unlinkSync(lockFile); } catch {}
  }
}

function buildPrBody({ finding, proposal }) {
  return [
    `**Self-Heal automated fix**`,
    ``,
    `**Detector:** ${finding.collector}`,
    `**Drift:** \`${finding.id}\` ${finding.table ? `on table \`${finding.table}\`` : ""} (${finding.kind}) in \`${finding.file}\``,
    ``,
    `**Root cause (model):** ${proposal.rootCause || "(not provided)"}`,
    ``,
    `**Risk level:** ${proposal.riskLevel || "unknown"}`,
    ``,
    `**Summary:** ${proposal.summary || ""}`,
    ``,
    `_Generated by \`scripts/_self_heal_loop.mjs\`. Guard CI is the final gate before merge._`,
  ].join("\n");
}
